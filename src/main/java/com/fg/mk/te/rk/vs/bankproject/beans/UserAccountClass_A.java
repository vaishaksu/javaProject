package com.fg.mk.te.rk.vs.bankproject.beans;

public class UserAccountClass_A {
	public int idusers;
	public String username;
	public String password;
	public String gender;
	public String fname;
	public String lastname;
	public String email;
	public int idaccounts;
	public double balance ;
	public int accountclosed;
	
	public int getIdusers() {
		return idusers;
	}
	public void setIdusers(int idusers) {
		this.idusers = idusers;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getIdaccounts() {
		return idaccounts;
	}
	public void setIdaccounts(int idaccounts) {
		this.idaccounts = idaccounts;
	}
	public double getBalance() {
		return balance;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}
	public int getAccountclosed() {
		return accountclosed;
	}
	public void setAccountclosed(int accountclosed) {
		this.accountclosed = accountclosed;
	}
}
